# Variables
# are used to store data, and are used to store data in a memory location, which can be used later in the code.

# Variables in Python are declared using the following syntax:
variable_name = 'value'

# Note:
# Variable names can contain letters, numbers, and underscores. They cannot start with a number or special character.
# Variable names are case-sensitive. This means that "Name" and "name" are two different variables.

# Different types of Datatypes are:

# Integers
a = 3
print(a)

# Float
b = 6.2
print(b)

# Boolean
c = True
print(c)

# String
d = 'Hello World'
print(d)

a1 = 'harry' # Single quotes can be used to write strings in Python
a2 = "harry" # Both single and double quotes can be used to write strings in Python
a3 = '''harry''' # Triple quotes can be used to write multi-line strings, like:

a4 = '''harry
is a good boy'''

print(a4)

# Complex
e = 3 + 5j
print(e)

# None
f = None
print(f)

# List - ordered and changeable collection of items
g = ['apple', 'banana', 'cherry']
h = [('apple', 5), ('banana', 6), ('cherry', 7)]
i = [1, 2, 3, 4, 5]

print(g)
print(h)
print(i)

