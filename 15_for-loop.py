# For Loop
# This is a simple for loop that iterates over a list of numbers and prints each number.

# Syntax:
# for variable in sequence:
    # code to execute (usually an indented block)

for i in range(5):
    print(i) # Output: 0 1 2 3 4


numbers = [1, 2, 3, 4, 5]
for num in numbers:
    print(num) # Output: 1 2 3 4 5



